pub fn output_file(
    lang: &Language,
    version: &Version,
    version_str: &str,
    path: &Path,
) -> Result<(), BumpError> {
    match lang {
        Language::C => c_output(version, path, version_str),
        Language::Go => go_output(version, path, version_str),
        Language::Java => java_output(version, path, version_str),
        Language::CSharp => csharp_output(version, path, version_str),
    }
}
