fn output_fields(version: &Version) -> Result<OutputFields, BumpError> {
    Ok(OutputFields {
        version_string: version.to_string(&PrintType::Regular)?,
        timestamp: version.timestamp.last.clone(),
        prefix: version.version.prefix.clone(),
        major: version.version.major,
        minor: version.version.minor.unwrap_or(0),
        patch: version.version.patch.unwrap_or(0),
        phase: version.phase.name.clone(),
    })
}

fn unsupported_mode(lang: &str, mode: &str) -> BumpError {
    BumpError::LogicError(format!(
        "Unsupported version type for {lang} output: {mode}"
    ))
}

fn write_output(lang: Language, path: &Path, content: String) -> Result<(), BumpError> {
    fs::write(path, content).map_err(BumpError::IoError)?;
    println!("{} written to {}", lang.file_description(), path.display());
    Ok(())
}

fn c_output(version: &Version, path: &Path) -> Result<(), BumpError> {
    let f = output_fields(version)?;
    let content = match version.version.mode.as_str() {
        "calver" => format!(
            r#"{BANNER_C}

#ifndef BUMP_VERSION_H
#define BUMP_VERSION_H

#define VERSION_STRING "{}"
#define VERSION_TIMESTAMP "{}"

#endif /* BUMP_VERSION_H */
"#,
            f.version_string, f.timestamp
        ),
        "semver" => format!(
            r#"{BANNER_C}

#ifndef BUMP_VERSION_H
#define BUMP_VERSION_H

#define VERSION_PREFIX "{}"
#define VERSION_MAJOR {}
#define VERSION_MINOR {}
#define VERSION_PATCH {}
#define VERSION_PHASE "{}"
#define VERSION_STRING "{}"
#define VERSION_TIMESTAMP "{}"

#endif /* BUMP_VERSION_H */
"#,
            f.prefix, f.major, f.minor, f.patch, f.phase, f.version_string, f.timestamp
        ),
        mode => return Err(unsupported_mode("C", mode)),
    };

    write_output(Language::C, path, content)
}

fn go_output(version: &Version, path: &Path) -> Result<(), BumpError> {
    let f = output_fields(version)?;
    let content = match version.version.mode.as_str() {
        "calver" => format!(
            r#"{BANNER_GO}

package version

const (
	STRING = "{}"
    TIMESTAMP = "{}"
)
"#,
            f.version_string, f.timestamp
        ),
        "semver" => format!(
            r#"{BANNER_GO}

package version

const (
	PREFIX    = "{}"
	MAJOR     = {}
	MINOR     = {}
	PATCH     = {}
	PHASE     = "{}"
	STRING    = "{}"
    TIMESTAMP = "{}"
)
"#,
            f.prefix, f.major, f.minor, f.patch, f.phase, f.version_string, f.timestamp
        ),
        mode => return Err(unsupported_mode("Go", mode)),
    };

    write_output(Language::Go, path, content)
}

